<template>
  <Top :store-brand-index="storeBrand.value" />
</template>

<script>
import { mapGetters } from "vuex";
import Top from "@/components/pages/topcontent/Top";

export default {
  components: {
    Top,
  },
  computed: {
    ...mapGetters({
      enums: "master/enums",
    }),
    storeBrand() {
      return this.enums.common__store_brand.find(
        ({ key }) => key.toLowerCase() === this.$route.params.brand
      );
    },
  },
  head() {
    return {
      title: this.storeBrand.label + "｜YAMADAYA onlinestore",
      meta: [
        {
          hid: "description",
          name: "description",
          content:
            this.storeBrand.label +
            "オンラインショップです。YAMADAYA公式オンライン通販サイトでは、オリジナルブランドから人気のセレクトブランドまで多数取り揃えております。",
        },
      ],
    };
  },
};
</script>
